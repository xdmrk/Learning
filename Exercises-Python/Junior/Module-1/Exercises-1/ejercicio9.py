#Un proveedor de internet mide la velocidad durante tres d√≠as consecutivos. Se debe calcular el promedio de velocidad registrado.
d1 = float(input("Digite velocidad registrada del dia 1: "))
d2 = float(input("Digite velocidad registrada del dia 2: "))
d3 = float(input("Digite velocidad registrada del dia 3: "))

promedio = (d1 + d2 + d3) / 3

print("El promedio de velocidad registrado es ", promedio)